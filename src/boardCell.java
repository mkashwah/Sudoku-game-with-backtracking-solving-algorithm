public class boardCell {
    private int i;
    private int j;
    private int val;

    public boardCell(int i, int j, int val){
        this.i = i;
        this.j = j;
        this.val = val;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public int getVal() {
        return val;
    }
}
